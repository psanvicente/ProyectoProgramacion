package catalogoHW;

public class Producto {
int precio;
char marca;
char modelo;
char descripcion;
int cantidad;

	Producto (int precio, char nRef, char modelo, char descripcion, int cantidad){
	this.precio = precio;
	this.marca = nRef;
	this.modelo = modelo;
	this.descripcion = descripcion;
	this.cantidad = cantidad;
	
}

}
